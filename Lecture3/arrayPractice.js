let value1 = ["Apple", 1 , false];

//Push and pop
console.log("\n********Push and pop********\n");
console.log(value1.push("new value"));
console.log(value1);
console.log(value1.pop()); //Remove last value
console.log(value1);

//Shift and unshift
console.log("\n********Shift and unshift********\n");
console.log(value1.unshift("new value"));
console.log(value1);
console.log(value1.shift()); //Remove first value
console.log(value1);
